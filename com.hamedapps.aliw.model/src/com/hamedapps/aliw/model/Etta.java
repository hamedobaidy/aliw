package com.hamedapps.aliw.model;

/**
 * @author Hamed Mohammadi
 *
 */
public class Etta {
	private double x, z;
	
	public Etta(double x, double z) {
		this.x = x;
		this.z = z;
	}

	/**
	 * @return the x
	 */
	public double getX() {
		return x;
	}

	/**
	 * @param x the x to set
	 */
	public void setX(double x) {
		this.x = x;
	}

	/**
	 * @return the z
	 */
	public double getZ() {
		return z;
	}

	/**
	 * @param z the z to set
	 */
	public void setZ(double z) {
		this.z = z;
	}

}
